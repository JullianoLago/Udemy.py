# Operadores aritméticos
Adicao = 10 + 10
print ("Adicao =", Adicao)

Subtracao = 10 - 9
print ("Subtração =", Subtracao)


Multiplicacao = 10 * 9
print ("Multiplicacao =", Multiplicacao)


Divisao = 10 / 2.2     ## O resultado sempre vai ser um float. [1.0]
print ("Divisao =", Divisao)



Divisao_inteira = 10 // 2  ## Arredonda o resultado para número inteiro
print ("Divisao_inteira =", Divisao_inteira)


Exponenciacao = 2 ** 9 ## Um número elevado a outro
print ("Exponenciacao =", Exponenciacao)

Modulo = 55 % 2 ## Resto da divisão
print ("Modulo", Modulo)


Concatenacao = "Julliano" + " " + "Lago"
print(Concatenacao)


a_dez_vezes = "A" * 10
tres_vezes_Julliano = "Julliano" * 3
print(a_dez_vezes)
print(tres_vezes_Julliano)

